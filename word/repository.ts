export function getWordsArray(): Promise<Word[][]> {
  return Promise.resolve<Word[][]>(wordsDB);
}
export function getWordsArrayByID(id: number): Promise<Word[]> {
  return Promise.resolve<Word[]>(wordsDB[id]);
}

export function createWord(word: Word): Promise<Word> {
  if (wordsDB[word.categoryId] === undefined) {
    const arr: Word[] = [];
    arr.push(word);
    wordsDB.push(arr);
  } else {
    wordsDB[word.categoryId].push(word);
  }
  return Promise.resolve(word);
}

export function updateWord(editWord: Word): Promise<Word> {
  const itemIndex = wordsDB[editWord.categoryId].findIndex(
    (it) => it.word.toLowerCase() === editWord.word.toLowerCase()
  );
  if (itemIndex < 0) {
    return Promise.reject(new Error('Word not found'));
  }
  const existsItem = wordsDB[editWord.categoryId].splice(itemIndex, 1)[0];
  const newItem: Word = {
    ...existsItem,
    ...editWord,
  };
  wordsDB[editWord.categoryId].push(newItem);
  return Promise.resolve(newItem);
}

export function deleteWord(idCategory: number): Promise<void> {
  wordsDB.splice(idCategory, 1);
  return Promise.resolve();
}
